SCORE g_score; //実体(プーリング)

//===================================================
//初期化
//===================================================
void InitializeScore(void)
{
	//値の初期化
	g_score.xPos = 0.0f;
	g_score.yPos = 0.0f;
	g_score.point = 0;

	//スコアのセット
	SetScore(7, 1);
}

//===================================================
//更新
//===================================================
void UpdateScore(void)
{
	//カンスト判定
	if (g_score.point >= 99999)
	{
		g_score.point = 99999;
	}
}
	

