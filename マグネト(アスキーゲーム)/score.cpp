//===================================================
//スコアのセット(セッター)
//===================================================
void SetScore(float px, float py)
{
	g_score.xPos = px;
	g_score.yPos = py;
}

//===================================================
//スコアの加算
//===================================================
void AddScore(int n)
{
	g_score.point += n;
}